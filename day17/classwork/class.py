# tuple არის მონაცემთა კოლექჩია, რომელიც ინახავს რამდენიმე მნიშვნელობას ერთ ცვლადში
# tuple არის:
# 1) დალაგებული
# 2) შეუცვლელი (immutable)
# 3) შეიძლება შეიცავდეს სხვადასხვა ტიპის მონაცემებს

# tuple-ს ვიყენებთ მაშინ, როდესაც გვინდა დავიცვათ მონაცემები შეცვლისგან.

# list:
# იცვლება (mutable)
# იყენებს კუთხურ ფრჩხილებს []
# აქვს მრავალი მეთოდი (append, remove, pop, clear, sort, reverse...)

numbers = (1, 2, 3, 4, 5)

print(len(numbers)) #გამოიყენება ელემენტების რაოდენობის გასაგებად

print(numbers[0]) #გამოიყენება ელემენტზე წვდომისთვის

print(numbers.count(1)) #გამოიყენება ელემენტის გამოყენების რაოდენობის გასაგებად

print(numbers.index(3)) #გამოიყენება ელემენტის ინდექსის გასაგებად

# numbers.append(6)
# numbers.remove(2)
